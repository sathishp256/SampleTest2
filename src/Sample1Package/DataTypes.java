package Sample1Package;

public class DataTypes {

	public static void main(String[] args) {
	
		//Numeric Data Types
		
		//Int
		int a=100, b=200;
		System.out.println("The value of a is"+a);
		System.out.println("The value of b is"+b);
		System.out.println("The value of total is"+(a+b));
		
		//Byte
		
		Byte C=125;
		Short D=32767;
		Long E=1234567891234567891L;
		System.out.println(E);
		
		// Decimal Data Types
		
		//Float f=10.23f;
		//Double K=10.223453425345123;
		
		//char
		
		//char q='F';
		
		boolean s=true;
		
		//String S="SSSS";
		
		System.out.println(s);
		
	}

}
